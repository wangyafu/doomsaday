export function buildEndingPrompt(
    daysSurvived: number,
    highLightMoment: string,
    finalStats: Stats,
    finalInventory: InventoryItem[],
    history: HistoryEntry[],
    profession: Profession | null
): string {
    const fullHistory = formatHistory(history, 15); // maxDays=15
    const isVictory = daysSurvived >= 20;
    const endingType = isVictory ? "【通关】恭喜，你在末世中存活了20天，等到了军队救援！" : "【死亡】游戏结束";

    return `
<game_summary>
## 游戏结束 - ${endingType}

### 玩家职业
${formatProfession(profession)}

### 存活天数
${daysSurvived} 天

### 最终状态
${formatStats(finalStats)}

### 最终背包
${formatInventory(finalInventory)}

### 高光时刻（如果有）
${highLightMoment || "没有特别突出的高光时刻"}

### 完整经历回顾
${fullHistory}
</game_summary>

<instruction>
请为这位玩家生成结局评价。

分析步骤（不要输出）：
1. 回顾整个游戏历程，找出关键转折点
2. 分析玩家的决策风格（保守/激进/随机）以及道德倾向（善良/中立/邪恶）
3. 如果死亡，找出直接死因和根本原因
4. 识别玩家展现的正面品质（智慧/勇敢/善良）和负面品质（愚蠢/懦弱/邪恶）
5. 构思一个精准的人设词
6. 根据玩家的品质选择合适的评价基调（赞美/嘲讽/惋惜/冷峻）
7. 根据历史记录计算五维评分

输出要求：
- 评语要有分寸感：对愚蠢和邪恶犀利嘲讽，对智慧、勇敢和善良真诚赞美
- 评语要有金句感，让人想分享
- 雷达图评分要有依据
- 返回纯JSON格式
</instruction>
`;
}
